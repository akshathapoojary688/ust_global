class Condition3
{
	public static void main(String[] args)
	{
		int marks =60;
		if(marks>=70)
		{
			System.out.println("fcd");
		}
		else if(marks>=60)
		{
			System.out.println("first class");
		}
		else if(marks>=50)
		{
			System.out.println("second class");
		}
		else
		{
			System.out.println("fail");
		}
	}
}