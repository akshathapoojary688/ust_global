class Switch
{
	public static void main(String[] args)
	{
		int marks =70;
		switch(marks)
		{
			case 40 : System.out.println("marks is fcd ");
				break;
			case 60 : System.out.println("marks is first class ");
				break;
			case 50 : System.out.println("marks is second class ");
				break;
			case 70 : System.out.println("marks is fcd ");
			
		}
	}
}